package pcrashstatistics;

/**
 * An Enumeration to represent the different injury types
 * @author Clayton Davidson
 *
 */
public enum InjuryType {
	/**
	 * The crash victim died
	 */
	FATAL,
	/**
	 * The crash victim was seriously injured and is in critical condition
	 */
	SERIOUS,
	/**
	 * The crash victim received minor injuries
	 */
	MINOR
}
